# If a runner runs 10 miles in 30 minutes and 30 seconds,
# What is their average speed in kilometers per hour?
# (Tip: 1 mile = 1.6 km)

kilometers = 10 * 1.6
per_hour = 60 / 30.5
kilometers_per_hour = kilometers * per_hour
print(kilometers_per_hour)

